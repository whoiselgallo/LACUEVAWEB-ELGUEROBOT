function cargarPosts() {
  const container = document.getElementById("posts-container");
  container.innerHTML = "";

  posts.forEach(post => {
    const card = document.createElement("div");
    card.classList.add("post-card");

    card.innerHTML = `
      <img src="${post.imagen}" class="post-img" alt="${post.titulo}">
      <h3>${post.titulo}</h3>
      <span class="fecha">${post.fecha} · ${post.autor}</span>
      <p>${post.contenido.substring(0, 180)}...</p>
      <button class="leer-mas" onclick="verArticulo('${post.titulo}')">Leer más</button>
    `;

    container.appendChild(card);
  });
}

// ===============================
// FUNCIÓN PARA VER ARTÍCULO COMPLETO
// ===============================

function verArticulo(titulo) {
  const articulo = posts.find(p => p.titulo === titulo);
  localStorage.setItem("articuloActual", JSON.stringify(articulo));
  window.location.href = "articulo.html"; // Cambia si usas otra ruta
}

cargarPosts();


